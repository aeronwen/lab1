package org.kp.lab1;

/**
 * Интерфейс субъекта (Subject) в паттерне "Наблюдатель".
 *
 * Субъект хранит список наблюдателей (IObserver) и при изменении своего состояния
 * уведомляет их вызовом update(). Наблюдатели подписываются через attach() и
 * отписываются через detach().
 *
 * Реализует: TimeServer (сервер времени — счётчик секунд и таймер).
 */
public interface Subject {

    /** Уведомить всех зарегистрированных наблюдателей об изменении состояния. */
    void notifyAllObservers();

    /** Добавить наблюдателя в список (подписать на уведомления). */
    void attach(IObserver observer);

    /** Удалить наблюдателя из списка (отписать от уведомлений). */
    void detach(IObserver observer);

    /** Текущее состояние субъекта (у TimeServer — число секунд с момента старта/сброса). */
    int getState();

    /**
     * Запущен ли основной таймер субъекта.
     * Используется компонентами, чтобы знать, активен ли отсчёт времени.
     */
    boolean isRunning();
}
