package org.kp.lab1;

/**
 * Интерфейс наблюдателя (Observer) в паттерне "Наблюдатель".
 *
 * Наблюдатель — объект, который реагирует на изменение состояния субъекта (Subject).
 * Используется пассивная модель: субъект вызывает update() без параметров,
 * а наблюдатель сам запрашивает нужные данные у субъекта через getState().
 *
 * Реализуют: ComponentOne, ComponentTwo, ComponentThree и анонимный наблюдатель
 * для метки состояния времени в MainController.
 */
public interface IObserver {

    /**
     * Вызывается субъектом при каждом изменении его состояния (каждую секунду тика TimeServer).
     * Наблюдатель внутри метода сам обращается к субъекту за данными (getState(), isRunning() и т.д.)
     * и обновляет свой вид или логику.
     */
    void update();
}
